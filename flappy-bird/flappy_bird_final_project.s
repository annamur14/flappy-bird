// UpdateSprite: updates the positions and velocities in the sprite struct
.align 2
UpdateSprite:
	push {r4, r5, r6, r7, r8, r9, r10, r11, r12, lr}
	 
	ldr r12, [r0, #PIXMAP_PTR] 
	
	ldrsh r8, [r12, #PIXMAP_WIDTH] 
	ldrsh r9, [r12, #PIXMAP_HEIGHT] 
	
	ldrh r6, =PIX_WIDTH
	ldrsh r6, [r6]

	ldrsh r4, [r0, #XPOS]
	ldrsh r1, [r0, #XVEL]
	add r4, r4, r1
	
	mov r7, r8, asr #1 
    
	sub r10, r4, r7 
	strh r10, [r0, #LEFTX] 
	
	add r10, r4, r7
    strh r10, [r0, #RIGHTX] 
	
	ldrsh r5, [r0, #YPOS]
	ldrsh r2, [r0, #YVEL]
	add r5, r5, r2
	
	mov r3, r9, asr #1 
	
	sub r11, r5, r3 
    strh r11, [r0, #TOPY] 
    
    add r11, r5, r3
    strh r11, [r0, #BOTTOMY] 
	
	ldrsh r10, [r0, #LEFTX]
	cmp r10, #0
	bge UpdateSprite0
	mov r10, #0
	strh r10, [r0, #LEFTX]
	mov r4, r7
	strh r4, [r0, #XPOS]
	rsb r1, r1, #0
	strh r1, [r0, #XVEL]
	
	UpdateSprite0:
		ldrsh r10, [r0, #RIGHTX]
		cmp r10, r6
		ble UpdateSprite2
		mov r10, r6
		strh r10, [r0, #RIGHTX]
		sub r4, r10, r7 
		strh r4, [r0, #XPOS]
		rsb r1, r1, #0
		strh r1, [r0, #XVEL]
	
	UpdateSprite2:
		ldrsh r11, [r0, #BOTTOMY]
		cmp r11, #209
		ble UpdateSprite3
		mov r11, #209
		strh r11, [r0, #BOTTOMY]
		sub r5, r11, r3 
		strh r5, [r0, #YPOS] 
		mov r2, #0 
		strh r2, [r0, #YVEL]
		bl GameOver
	
	UpdateSprite3:
		strh r1, [r0, #XVEL]
		add r2, r2, #1
		strh r2, [r0, #YVEL]
		strh r4, [r0, #XPOS]
		strh r5, [r0, #YPOS]

	pop {r4, r5, r6, r7, r8, r9, r10, r11, r12, pc}
.pool

//DrawBackground: draws ground platform
DrawBackground:
	push {r4, r5, r6, r7, r8, lr}
	mov r2, r0
	mov r0, #0 // initial x value
	mov r1, #209 // initial y value
	// ldr r2 // color
	mov r4, #240 // 60 rows in buffer
	mov r5, #320 // 80 cols in buffer
	mov r6, #0 // initialize i
	mov r7, #210 // initialize j
	b InnerLoopBackground
	
	OuterLoopBackground:
		add r7, r7, #1 // increment j
		// end if j==240
		cmp r7, r4
		beq BackgroundDone
		mov r6, #0 // reset i
		InnerLoopBackground:
		// draw char
		// increment counter
		// compare counter with r4 -> larger=move on to next row & reset counter
			mov r0, r6
			mov r1, r7
			bl DrawPix
			add r6, r6, #1
			
			cmp r6, r5 // compare i and 320
			bne InnerLoopBackground // loop again if theyre not equal
			beq OuterLoopBackground // branch to outerloop if they are equal
	BackgroundDone:
	pop {r4, r5, r6, r7, r8, pc}
	

// UpdatePipe: update pipe's position based on info in its struct
.align 2
UpdatePipe:
	push {r4, r5, r6, r7, r8, r9, r10, r11, r12, lr}
	
	ldr r12, [r0, #PIXMAP_PTR] // load addr to pixmap
	
	// load pixmap width and height
	ldrsh r8, [r12, #PIXMAP_WIDTH] // r8 <- pixmap_width 
	ldrsh r9, [r12, #PIXMAP_HEIGHT] // r9 <- pixmap_height
	
	ldrh r6, =PIX_WIDTH // address of PIX_WIDTH constant
	ldrsh r6, [r6]

	// new_xpos = structptr[r0].xpos + structptr.xvel
	ldrsh r4, [r0, #XPOS]
	ldrsh r1, [r0, #XVEL]
	sub r4, r4, r1
	
	// calculate leftx and rightx bounds
	mov r7, r8, asr #1 // divide r8(width) by 2
    
	sub r10, r4, r7 // x - width/2
	strh r10, [r0, #LEFTX] // struct.leftx = x - width/2
	
	add r10, r4, r7
    strh r10, [r0, #RIGHTX] // struct.rightx = x + width/2
	
	// new_ypos = structptr.ypos + structptr.yvel
	ldrsh r5, [r0, #YPOS]
	
	// calculate topy and bottomy bounds
	mov r3, r9, asr #1 // divide r9(height) by 2
	
	sub r11, r5, r3 // y - height/2
    strh r11, [r0, #TOPY] // struct.topy = y - height/2
    
    add r11, r5, r3
    strh r11, [r0, #BOTTOMY] // struct.bottomy = y + height/2
	
	// if (leftx < 0)
	ldrsh r10, [r0, #RIGHTX]
	cmp r10, #0
	bge UpdatePipe3
	// leftx ⇽ 0 ; structptr.xvel ⇽ - structptr.xvel
	mov r10, #0
	strh r10, [r0, #RIGHTX]
	mov r4, #660 // back of pipes
	strh r4, [r0, #XPOS]
	// increment score if its a top pipe
	//cmp r5, #32
	//bgt UpdatePipe3
	ldr r10, =Score
	ldr r11, [r10]
	add r11, r11, #1
	str r11, [r10]
	
	UpdatePipe3:
		//strh r1, [r0, #XVEL]
		strh r4, [r0, #XPOS]

	pop {r4, r5, r6, r7, r8, r9, r10, r11, r12, pc}
.pool

.align 2
UpdatePipes:
	push {r4, r5, r6, lr}
	// for each pipe: updatepipe, drawpipe, check collision with bird
	ldr r4, =PipeArray // ptr to PipeArray
	mov r5, #0 // counter 
	ldr r6, =BirdStruct
	MainPipeLoop:
		ldr r0, [r4, r5] // get ptr to specific pipe struct
		bl UpdatePipe
		bl DrawSprite
		//ldr r0, [r4, r5]
		//bl AddPoint
		mov r1, r6
		bl DetectCollision
		ldrh r0, =BACKGROUNDCOLOR
		bl DrawBackground
		add r5, r5, #4 
		cmp r5, #40 
		blt MainPipeLoop
	pop {r4, r5, r6, pc}
.pool
	
// GameOver: write "GAME OVER" and score if player loses game
// infinitely loop back to top of gameover
.align 2
GameOver:
	mov r0, #0
	bl ClearVGA
	bl ClearTextBuffer
	bl GameOverSound
	GameOverInfLoop:
		ldr r2, =GameOverString
		mov r0, #36
		mov r1, #28
		bl DrawStr
		ldr r2, =ScoreString
		mov r0, #37
		mov r1, #31
		bl DrawStr
		mov r0, #44
		mov r1, #31
		ldr r3, =Score
		ldr r3, [r3]
		lsr r2, r3, #1
		//mov r2, r3
		bl DrawNum
		b GameOverInfLoop
		
// GameOverSound: plays a sequence of notes when player loses the game
.align 2
GameOverSound:
	push {r4, r5, r6, r8, lr}
		mov r3, #0
		mov  r8, #0x14 // tone 
		ldr  r6, =0xff203040 // audio queue addr
		ldr  r4, =0x40000000//0x40000000 // volume i think
		mov  r5, r8
	OverWaitForWriteSpace:
		cmp r3, #100
		bge ChangeTone
		ldr  r2, [r6, #4]
		tst  r2, #0xff000000
		beq  OverWaitForWriteSpace // queue full, need to wait
		tst  r2, #0x00ff0000
		beq  OverWaitForWriteSpace
	OverWriteTwoSamples:
		str  r4, [r6, #8] // out left
		str  r4, [r6, #12] // out right
		subs r5, #1
		bne  OverWaitForWriteSpace
	OverHalfPeriodInvertWaveform:
		mov  r5, r8
		neg  r4, r4
		add r3, r3, #1
		b    OverWaitForWriteSpace
	ChangeTone:
		mov r3, #0
		mov  r8, #0x15 // tone 
		ldr  r6, =0xff203040 // audio queue addr
		ldr  r4, =0x40000000//0x40000000 // volume i think
		mov  r5, r8
	OverWaitForWriteSpace2:
		cmp r3, #100
		bge ChangeToneAgain
		ldr  r2, [r6, #4]
		tst  r2, #0xff000000
		beq  OverWaitForWriteSpace2// queue full, need to wait
		tst  r2, #0x00ff0000
		beq  OverWaitForWriteSpace2
	OverWriteTwoSamples2:
		str  r4, [r6, #8] // out left
		str  r4, [r6, #12] // out right
		subs r5, #1
		bne  OverWaitForWriteSpace2
	OverHalfPeriodInvertWaveform2:
		mov  r5, r8
		neg  r4, r4
		add r3, r3, #1
		b    OverWaitForWriteSpace2
	ChangeToneAgain:
		mov r3, #0
		mov  r8, #0x1c // tone 
		ldr  r6, =0xff203040 // audio queue addr
		ldr  r4, =0x40000000//0x40000000 // volume i think
		mov  r5, r8
	WaitForWriteSpace3:
		cmp r3, #200
		bge OverDone
		ldr  r2, [r6, #4]
		tst  r2, #0xff000000
		beq  WaitForWriteSpace3// queue full, need to wait
		tst  r2, #0x00ff0000
		beq  WaitForWriteSpace3
	WriteTwoSamples3:
		str  r4, [r6, #8] // out left
		str  r4, [r6, #12] // out right
		subs r5, #1
		bne  WaitForWriteSpace3
	HalfPeriodInvertWaveform3:
		mov  r5, r8
		neg  r4, r4
		add r3, r3, #1
		b    WaitForWriteSpace3
	OverDone:
	pop {r4, r5, r6, r8, pc}

		
		
// PlaySound: plays a short tone - used for button being pressed/bird jumping
.align 2	
PlaySound:
	push {r4, r5, r6, r7, r8, r9, lr}
		mov r7, #0
		mov  r8, #0xf // tone 
		ldr  r6, =0xff203040 // audio queue addr
		ldr  r4, =0x40000000//0x40000000 // volume i think
		mov  r5, r8
	WaitForWriteSpace:
		cmp r7, #35 // # of times this loop runs -> duration of tone
		bge SoundDone
		ldr  r9, [r6, #4]
		tst  r9, #0xff000000
		beq  WaitForWriteSpace // queue full, need to wait
		tst  r9, #0x00ff0000
		beq  WaitForWriteSpace
	WriteTwoSamples:
		str  r4, [r6, #8] // out left
		str  r4, [r6, #12] // out right
		subs r5, #1
		bne  WaitForWriteSpace
	HalfPeriodInvertWaveform:
		mov  r5, r8
		neg  r4, r4
