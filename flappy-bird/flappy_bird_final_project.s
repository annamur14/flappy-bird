// DrawSprite: draws a sprite at the positions in its struct
.align 2
DrawSprite:
    push {r4, r5, lr} 
	mov r5, r0
    ldr r4, [r0, #PIXMAP_PTR] 
    ldrsh r1, [r0, #XPOS]      
    ldrsh r2, [r0, #YPOS]    
    mov r0, r4
    bl BitBlit             
	mov r0, r5
    pop {r4, r5, pc}
	
	
// DetectCollision: checks for collision and prints if collision detected
.align 2
DetectCollision:
    push {r4, r5, r6, r7, r8, r9, r10, r11, lr}

    ldrsh r4, [r0, #LEFTX]   
    ldrsh r5, [r0, #RIGHTX]  
    ldrsh r6, [r0, #TOPY]    
    ldrsh r7, [r0, #BOTTOMY] 

    ldrsh r8, [r1, #LEFTX]
    ldrsh r9, [r1, #RIGHTX]
    ldrsh r10, [r1, #TOPY]
    ldrsh r11, [r1, #BOTTOMY]

    cmp r4, r9   
    bgt NoCollision
    cmp r5, r8   
    blt NoCollision

    cmp r6, r11 
    bgt NoCollision
    cmp r7, r10 
    blt NoCollision

	bl GameOver
	b CollisionDone

NoCollision:
CollisionDone:
    pop {r4, r5, r6, r7, r8, r9, r10, r11, pc}



// DrawStr: DrawStr(x(r0), y(r1), s(r2)) - Draws a string (s – null terminated) at screen column x, row y.
.align 2
DrawStr:
	push {r4, r5, lr}
	mov r5, r2
	ldrb r4, [r5] 
	b IsCharNull
	Beans:
		mov r2, r4
		bl DrawChar
		add r5, r5, #1
		ldrb r4, [r5] 
		
		cmp r4, #0
		beq IsCharNull

		add r0, r0, #1 
	IsCharNull:
		cmp r4, #0
		bne Beans 
		
	pop {r4, r5, pc}
	
	
	
	

// ButtonPressedCheck: checks if button 0 is pressed
.align 2
ButtonPressedCheck:
	push {r4, r5, r6, r7, r8, lr}
	
	ldr r0, =BirdStruct 
	ldr r1, =BUTTON_ADDR 
	ldr r4, [r1] 
	ldr r2, =IsButtonPressed 
	ldr r3, [r2] 
	
	cmp r4, r3
	beq ButtonCheckDone
	bgt Bounce
	mov r7, #0
	str r7, [r2] 
	b ButtonCheckDone
	
	Bounce:
	bl PlaySound
	ldrsh r7, [r0, #YVEL]
	cmp r7, #0
	beq ButtonCheckDone 
	blt Up
	rsb r7, r7, #0
	b BounceDone
	
	Up: 
	bl PlaySound
	ldr r8, =OrigYvel 
	ldrh r8, [r8]
	add r8, r8, r8
	mvn r7, r8
	b BounceDone
	
	BounceDone:
	strh r7, [r0, #YVEL]
	mov r7, #1 
	str r7, [r2]
	b ButtonCheckDone
	
	ButtonCheckDone:
	pop {r4, r5, r6, r7, r8, pc}
.pool

