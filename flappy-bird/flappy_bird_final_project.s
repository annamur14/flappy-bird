// ComputeDigit(k(r0)): computes digit needed for DrawNum
ComputeDigit:
	push {r4, r5, r6, r8, lr}
	
	mov r4, #0
	mov r6, r0
	mov r5, r0 
	b IsRemLDivisor
	
	Body:
		sub r5, r5, #10 
		add r4, r4, #1 
		
	IsRemLDivisor:
		cmp r5, #10 
		bge Body
		
		lsl r8, r4, #3 
		add r8, r8, r4 
		add r8, r8, r4 
		
		sub r5, r6, r8 
		
		mov r0, r5
		
	pop {r4, r5, r6, r8, pc}
	
// DrawNum: DrawNum(x(r0), y(r1), n(r2)) - Draws an integer (decimal/base 10) value (n – signed int) at
// screen column x, row y. The integer value should be preceded by a “-” if the value is
// negative and nothing if it is zero/positive. Do not display any leading zeros. 
// text should be clipped around screen edges
DrawNum:
	push {r4, r5, r6, r7, r8, r9, r10, r11, lr}
	mov r6, r0 // x (r0)
	mov r7, r1 // y (r1)
	mov r8, r2 // n(r2)
	mov r11, #0 
	
	cmp r2, #0
	bne NotZero
	add r2, r2, #48
	bl DrawChar
	b DrawNumDone
	NotZero:
	PushLoop: 
	
		mov r0, r8 
		
		bl ComputeDigit 
		cmp r0, #0
		beq PopLoop
		push {r0}
		add r11, r11, #1 
		
		mov r9, r8
		mov r8, #0
		b IsRemLDivisor2

		DivideBody:
			sub r9, r9, #10 
			add r8, r8, #1 

		IsRemLDivisor2:
			cmp r9, #10 
			bge DivideBody
		
		b PushLoop
	PopLoop:
		b IsR10GT9
		MainLoop:
		pop {r0}
		mov r10, r0
		mov r2, r0 
		mov r0, r6 
		mov r1, r7
		add r2, r2, #48 
		bl DrawChar 
		add r6, r6, #1
		sub r11, r11, #1
		IsR10GT9:
			cmp r11, #0
			bne MainLoop
	DrawNumDone:
	pop {r4, r5, r6, r7, r8, r9, r10, r11, pc}

// DrawChar(x[r0], y[r1], c[r2]): draws a given character on the screen at the given coords
.align 2
DrawChar:
	push {r4, r5, r6, r7, lr}
	mov r6, #0
	mov r5, #0
	ldr r4, =TEXT_BUFFER
	cmp r0, #79
	bgt Done

	lsl r7, r1, #7 
	add r6, r7, r0 
	add r5, r6, r4 
	
	strb r2, [r5]
	
	Done:
	
	pop {r4, r5, r6, r7, pc}

// ClearTextBuffer() : none
// Clears the text buffer
.align 2
ClearTextBuffer:
	push {r4, r5, r6, r7, lr}
	mov r0, #0 
	mov r1, #0
	mov r2, #0x20 
	mov r4, #60 
	mov r5, #80 
	mov r6, #0 
	mov r7, #0 
	b InnerLoop
	
	OuterLoop:
		add r7, r7, #1 

		cmp r7, r4
		beq ClearTextBufferDone
		mov r6, #0 
		InnerLoop:
			mov r0, r6
			mov r1, r7
			bl DrawChar
			add r6, r6, #1
			
			cmp r6, r5 
			bne InnerLoop 
			beq OuterLoop 
	ClearTextBufferDone:
	pop {r4, r5, r6, r7, pc}
	
// DrawPix: write 1 pixel of color r2 to the screen at (x,y)
.align 2
DrawPix:
	push {r4, r5, r6, r7, r8, lr}
	mov r6, #0
	mov r5, #0
	
	ldr r4, =PIX_BUFFER
	
	cmp r0, #320
	bge DrawPixDone
	
	cmp r0, #0
	blt DrawPixDone
	
	cmp r1, #240
	bge DrawPixDone
	
	cmp r1, #0
	blt DrawPixDone
	
	lsl r7, r1, #9 
	add r6, r7, r0 
	lsl r8, r6, #1
	add r5, r8, r4 
	
	strh r2, [r5] 
	
	DrawPixDone:
	pop {r4, r5, r6, r7, r8, pc}
	
	
	
// ClearVGA( color : uint) : none
// Clears the video buffer by filling it with the given color value. 
// Colors as 16-bit values (zero extended to 32-bits for procedure call). 
// For instance, the value 0xffff would fill the screen with white pixels and
// the value 0x0000 would paint it black.
.align 2
ClearVGA:
	push {r4, r5, r6, r7, r8, lr}
	mov r2, r0
	mov r0, #0 
	mov r1, #0 
	mov r4, #240 
	mov r5, #320 
	mov r6, #0 
	mov r7, #0 
	b InnerLoopPix
	
	OuterLoopPix:
		add r7, r7, #1 
		cmp r7, r4
		beq ClearVGADone
		mov r6, #0 
		InnerLoopPix:
			mov r0, r6
			mov r1, r7
			bl DrawPix
			add r6, r6, #1
			
			cmp r6, r5 
			bne InnerLoopPix 
			beq OuterLoopPix 
	ClearVGADone:
	pop {r4, r5, r6, r7, r8, pc}


// BitBlit
// Draws a pixelmap (color image) on the screen, 
// given a pointer to the pixmap data structure. The image will be centered at (x,y).
.align 2
BitBlit:
	push {r4, r5, r6, r7, r8, r9, r10, r11, r12, lr}
	mov r4, r0
	mov r5, r1
	mov r6, r2
	mov r3, #0
	mov r12, #0

	ldrsh r8, [r4, #PIXMAP_WIDTH] 
	ldrsh r9, [r4, #PIXMAP_HEIGHT] 
    
	mov r10, r8, asr #1 
	sub r10, r5, r10
	
	
	mov r11, r9, asr #1 
	sub r11, r6, r11 
	
	ldrh r7, [r4, #PIXMAP_TRANSPARENCY]
	
	mov r5, #0
	add r5, r5, #PIXMAP_PIXELDATA
	add r5, r5, r4 
	
	OuterLoopBitBlit:
		add r3, r3, #1 
		cmp r3, r9
		bgt End
		mov r12, #0 
		InnerLoopBitBlit:
			mov r0, #0
			mov r1, #0
			add r0, r12, r10
			add r1, r3, r11
			sub r1, r1, #1 
			ldrh r2, [r5]
			cmp r2, r7
			beq Transparent
			bl DrawPix
			Transparent:
			add r12, r12, #1
			add r5, r5, #2 
			
			cmp r12, r8 
			bne InnerLoopBitBlit 
			beq OuterLoopBitBlit 
	
	End:
		pop {r4, r5, r6, r7, r8, r9, r10, r11, r12, pc}                             
	


.align 2
init:
